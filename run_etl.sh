# run_etl.sh - скрипт запуска ETL-пайплайна на Linux/Mac

echo "================================================"
echo "ETL-пайплайн для курсов валют ЦБ РФ"
echo "Дата запуска: $(date '+%Y-%m-%d %H:%M:%S')"
echo "================================================"

# Активация виртуального окружения (если есть)
if [ -d ".venv" ]; then
    echo "Активация виртуального окружения..."
    source .venv/bin/activate
fi

# Проверка зависимостей
echo "Проверка Python..."
python --version

echo "Проверка зависимостей..."
pip list | grep -E "(zeep|psycopg2|requests)"

# Запуск ETL
echo "Запуск основного скрипта..."
python main.py "$@"

EXIT_CODE=$?

# Деактивация окружения
if [ -d ".venv" ]; then
    deactivate
fi

echo "================================================"
echo "ETL завершен с кодом: $EXIT_CODE"
echo "================================================"

exit $EXIT_CODE